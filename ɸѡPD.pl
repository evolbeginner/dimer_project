sub extract_PD
{
    open(IN,"../selected_seq/PD.txt");
    while(<IN>)
    {
           chomp;
           foreach (my @a=split)
           {
                    do {$_ =~ s/\-/\_/; $_=uc($_);} foreach (@a);
                    'PD' -> {$_} = 0;
           }
    }
}

sub haha
{
    foreach (@a)
    {
        $k++;
        $b=$_ if $k % 3 == 1;
        $c=$_ if $k % 3 == 2;
        $d=$_ if $k % 3 == 0;
        next if $k % 3 != 0;
        next if $d !~ /\d/;
        next if exists 'PD' -> {$b};
        print $c."\t".$d."\n";
    }
}

